# Открываем файл F1 для записи данных, которые вводит пользователь
with open("F1.txt", "w") as f1:
    while True:
        line = input("Введите строку (пустая строка для завершения): ")
        if not line:
            break
        f1.write(line + "\n")

# Открываем файл F1 для чтения и файл F2 для записи строк
with open("F1.txt", "r") as f1, open("F2.txt", "w") as f2:
    for line in f1:
        # Разбиваем строку на слова, используя пробел как разделитель
        words = line.split()
        # Создаем множество для хранения уникальных слов в строке
        unique_words = set(words)
        # Если количество уникальных слов меньше количества слов в строке, то
        # в строке есть как минимум два одинаковых слова
        if len(unique_words) < len(words):
            f2.write(line)

print("Готово. Строки с не менее чем двумя одинаковыми словами скопированы в файл F2.txt.")

